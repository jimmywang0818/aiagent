'use strict';

/**
 * seed-rules.js
 * 將從 SYSTEM_PROMPT 移出的規則寫入 DB。
 * 使用 upsertRule — 依 (brand_id, category, title) 判斷，已存在則更新，不存在則新增。
 * 安全重複執行。
 *
 * 用法：node scripts/seed-rules.js
 */

require('dotenv').config();
const db = require('../src/db');

// ── 取得品牌 ID ────────────────────────────────────
const damo = db.getBrands().find(b => b.name === '達摩');
if (!damo) {
  console.error('[seed-rules] 找不到「達摩」品牌，請確認 DB 已初始化。');
  process.exit(1);
}
const damoId = damo.id;

let inserted = 0, updated = 0;

function upsert(brandId, category, title, content) {
  const r = db.upsertRule(brandId, category, title, content);
  if (r.action === 'inserted') { inserted++; console.log(`  [+] ${title}`); }
  else                         { updated++;  console.log(`  [~] ${title}`); }
}

